            console.warn('‚ö†Ô∏è –û—à–∏–±–∫–∞ Supabase –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –ø—Ä–æ—Ñ–∏–ª—è:', error.message);
